let searchQuestion = document.getElementById("searchQuestion");
let mainbox = document.getElementById("mainbox");
let theQues = false;
let theAns = false;

let idval = 0;
quesans.forEach(element => {
    idval++;
    element.uniqueid = `id${idval}`;
    theQues = document.createElement("h1");
    theAns = document.createElement("p");
    theQues.classList.add(element.uniqueid)
    theAns.classList.add(`class${element.uniqueid}`)
    theQues.innerText = element.question;
    theAns.innerText = element.answer;
    mainbox.appendChild(theQues);
    mainbox.appendChild(theAns);
});

let myArr = [];

searchQuestion.addEventListener("input", ()=> {
    console.log(myArr);

    let myval = searchQuestion.value;
    myval = myval.toLowerCase();

    quesans.forEach(element => {

        element.question = element.question.toLocaleLowerCase();

        if (element.question.includes(myval)) {

            if (!myArr.includes(element)) {
                myArr.push(element)
            }

        } else{
            let indexim = myArr.indexOf(element);
            if (document.querySelector(`.${element.uniqueid}`)) {
                document.querySelector(`.${element.uniqueid}`).style.display = "none";
                document.querySelector(`.class${element.uniqueid}`).style.display = "none";
            }

            if (indexim !== -1) {
            // Remove the element using splice
            myArr.splice(indexim, 1);
            }
        };

    });

    myArr.forEach(element => {
        if (element) {
            if (document.querySelector(`.${element.uniqueid}`)) {
                document.querySelector(`.${element.uniqueid}`).style.display = "block";
                document.querySelector(`.class${element.uniqueid}`).style.display = "block";

            }
          }

    });
})
